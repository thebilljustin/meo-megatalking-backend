     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $store = Curric::create(request()->validate([
            'title' => 'required',
            'category' => 'required'
        ]));

        if ($store) { return response()->json(['message' => 'Added new curriculum']); }
